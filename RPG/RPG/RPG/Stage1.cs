        // Update method
        public void Update(GameTime gameTime)
        {
            switch (mapSelect)
            {
                // Region 1_1
                #region
                case "1_1":
                    #region
                    {
                        currentTextureMap = texture1_1;
                        currentMap = map1_1;
                        if (currentMap[playerY, playerX] == 2)
                        {
                            mapSelect = "1_1_tent1";
                            changePlayerX = 15;
                            changePlayerY = 11;
                        }

                        else if (currentMap[playerY, playerX] == 3)
                        {
                            mapSelect = "1_1_tent2";
                            changePlayerX = 15;
                            changePlayerY = 11;
                        }

                        else if (currentMap[playerY, playerX] == 4)
                        {
                            mapSelect = "1_2";
                            changePlayerX = 15;
                            changePlayerY = 1;
                        }
                        break;
                    }
                    #endregion

                case "1_1_tent1":
                    #region
                    {
                        currentTextureMap = texture1_1_tent1;
                        currentMap = map_tent1_1_1;
                        if (currentMap[playerY, playerX] == 2)
                        {
                            mapSelect = "1_1";
                            changePlayerX = 10;
                            changePlayerY = 5;
                        }
                        break;
                    }
                    #endregion

                case "1_1_tent2":
                    #region
                    {
                        currentTextureMap = texture1_1_tent2;
                        currentMap = map_tent2_1_1;
                        if (currentMap[playerY, playerX] == 2)
                        {
                            mapSelect = "1_1";
                            changePlayerX = 21;
                            changePlayerY = 5;
                        }
                        break;    
                    }
                    #endregion
                #endregion

                // Region 1_2
                #region
                case "1_2":
                    {
                        currentMap = map1_2;

                        break;
                    }
                #endregion

